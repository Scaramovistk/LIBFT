//#include "libft.h"
#include <unistd.h>

void	ft_putchar_fd(char c, int fd)
{
	write(fd, &c, 1);
}

	#include <stdio.h>
	int	main (void)
	{
		printf("\n");
		ft_putchar_fd('7', 0);
		printf("\n");
	}