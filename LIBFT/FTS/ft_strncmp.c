#include "libft.h"

int	ft_strncmp(const char *s1, const char *s2, size_t n)
{
	size_t	row;

	row = 0;
	while ((s1[row] != '\0' || s2[row] != '\0') && row < n)
	{
		if (s1[row] != s2[row])
			return (s1[row] - s2[row]);
		row++;
	}
	return (0);
}
