#include "libft.h"

void	ft_putendl_fd(char *s, int fd)
{
	int	row;

	row = 0;
	while (s[row] != '\0')
	{
		ft_putchar_fd(s[row], fd);
		row++;
	}
	ft_putchar_fd('\n', fd);
}
